/* Define a variable of type struct AMMessage. The examples below demonstrate
   how to pass the whole variable through the queue, and as the structure is
   moderately large, also how to pass a reference to the variable through a queue. */
struct AMessage
{
    char ucMessageID;
};
struct AMessage xMessage;




//Task Area!The user must create task handle manually because of debugging and specification
TaskHandle_t tcbTask1 = NULL;
TaskHandle_t tcbTask2 = NULL;

Mutex_Handle mutex = NULL;
//!!!priority inversion test

void taskA( )
{
    while (1) {
        mutex_lock(mutex, 1);
        xMessage.ucMessageID = 0xab;

        mutex_unlock(mutex);
        TaskDelay(1);

        /*
        HAL_GPIO_WritePin(GPIOC, GPIO_PIN_13, GPIO_PIN_RESET);
        TaskDelay(1000);*/
    }
}

void taskB( )
{
    while (1) {
        schedule();
    }
}


void taskC( )
{
    while (1) {
        mutex_lock(mutex, 1);
        xMessage.ucMessageID = 0x02;
        schedule();//!!!priority inversion test.A will be interrupted by B
        mutex_unlock(mutex);

        TaskDelay(1);
    }
}




void APP( )
{
    mutex = mutex_creat();
    xTaskCreate(    taskA,
                    256,
                    NULL,
                    3,
                    &tcbTask1
    );

    xTaskCreate(    taskB,
                    256,
                    NULL,
                    2,
                    &tcbTask2
    );

    xTaskCreate(    taskC,
                    256,
                    NULL,
                    1,
                    &tcbTask2
    );

}


